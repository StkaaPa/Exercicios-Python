# Faça um Programa que calcule a área de um quadrado, em seguida mostre o dobro desta área para o usuário.
num = float(input("Introduza o valor de um dos lados do quadrado: "))
area = num * 2
area2 = area * 2
print("O valor da área do quadrado é de", area, "metros.")
print("O dobro do valor da área é de", area2, "metros.")
