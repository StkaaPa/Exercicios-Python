# Faça um Programa que peça as 4 notas bimestrais e mostre a média.

x = int(input("Digite a primeira nota bimestral de português: "))
x = int(input("Digite a segunda nota bimestral de português: "))
y = int(input("Digite a primeira nota bimestral de matemática: "))
y = int(input("Digite a segudna nota bimestral de matemática: "))
media = ((x + x + y + y)/4)
print("A média das notas bimestrais é: ", media)
